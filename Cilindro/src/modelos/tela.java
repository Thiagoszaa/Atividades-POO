    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
             
            float Altura = Float.parseFloat(jTextField2altura.getText());
            float Raio = Float.parseFloat(jTextField1raio.getText());
            
            Cilindro cilindro = new Cilindro ( Raio, Altura  );
            
            cilindro.setraio (Raio);
            cilindro.setaltura (Altura);
            
            String saida = "Calculos \n"; 
             saida += "Calculo do Volume: \n" + String.format("%.2f", cilindro.calcularVolume()) + "\n";
             saida += "Calculo da Area Lateral: \n" + String.format("%.2f", cilindro.calcularAreaLateral()) + "\n";
             saida += "Calculo da Area total: \n" + String.format("%.2f", cilindro.calcularAreaTotal()) + "\n";
             
            jTextArea1saida.setText(saida);
            } catch (Exception e) {
                jTextArea1saida.setText("Erro ao calcular: " + e.getMessage());
            }
      
        
    }//GEN-LAST:event_jButton1ActionPerformed

